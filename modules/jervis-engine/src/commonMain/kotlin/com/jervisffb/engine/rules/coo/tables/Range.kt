package com.jervisffb.engine.rules.coo.tables

/**
 * Enumeration of all ranges used for passing across all rulesets.
 *
 * See page 48 in the BB2020 rulebook.
 */
enum class Range {
    PASSING_PLAYER, // Starting point
    QUICK_PASS,
    SHORT_PASS,
    LONG_PASS,
    LONG_BOMB,
    OUT_OF_RANGE
}
