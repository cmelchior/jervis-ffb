package dk.ilios.jervis.ui.model

/**
 * This package contains the UI equivalent classes of models found in `dk.ilios.jervis.model`. This allows us
 * to extend their functionality with UI specific states and keeps a clean seperation between the rules and UI layer.
 *
 * No UI elements should access models in `dk.ilios.jervis.model` directly. All state information should instead be
 * fetched from classes in this package
 */